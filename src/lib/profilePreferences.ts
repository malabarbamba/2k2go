const PROFILE_COUNTRY_SET = new Set<string>(PROFILE_COUNTRIES);

export const isSupportedProfileCountry = (
	value: string | null | undefined,
): boolean => {
	const normalizedValue = typeof value === "string" ? value.trim() : "";
	return PROFILE_COUNTRY_SET.has(normalizedValue);
};
