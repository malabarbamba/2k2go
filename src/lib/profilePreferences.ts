export const PROFILE_NEW_CARDS_PER_DAY_MIN = 10;
export const PROFILE_NEW_CARDS_PER_DAY_MAX = 20;
export const PROFILE_NEW_CARDS_PER_DAY_DEFAULT = 20;

export const clampProfileNewCardsPerDay = (value: number): number => {
	if (!Number.isFinite(value)) {
		return PROFILE_NEW_CARDS_PER_DAY_DEFAULT;
	}

	return Math.min(
		PROFILE_NEW_CARDS_PER_DAY_MAX,
		Math.max(PROFILE_NEW_CARDS_PER_DAY_MIN, Math.round(value)),
	);
};

